int	ft_atoi(const char *str)
{
	int	i;
	int	j;
	int	x;

	j = 1;
	i = 0;
	x = 0;
	while ((*(str + i) == ' ' || *(str + i) == '\t')
		|| (*(str + i) == '\n' ) || (*(str + i) == '\v')
		|| (*(str + i) == '\f') || (*(str + i) == '\r'))
		i++;
	if (*(str + i) == '-' || *(str + i) == '+')
	{
		if (*(str + i) == '-')
			j = -j;
		i++;
	}
	while (*(str + i) >= '0' && *(str + i) <= '9')
	{
		x = x * 10 + (*(str + i) - 48);
		i++;
	}
	return (x * j);
}
