int	ft_isalnum(int c)
{
	int	x;

	if ((c >= '0' && c <= '9') || (c >= 'a' && c <= 'z')
		|| (c >= 'A' && c <= 'Z'))
	{
		x = 1;
	}
	else
		x = 0;
	return (x);
}
